# Colores y estilos ANSI para la consola
RESET = "\033[0m"
BOLD = "\033[1m"

RED = "\033[31m"
GREEN = "\033[32m"
YELLOW = "\033[33m"
BLUE = "\033[34m"
MAGENTA = "\033[35m"
CYAN = "\033[36m"

# Atajos semánticos
HEADER = BOLD + CYAN
SUBHEADER = BOLD + MAGENTA
OK = GREEN
WARNING = YELLOW
ERROR = BOLD + RED
